namespace SecureTextEditor.GUI.Dialog {
    /// <summary>
    /// Describes the types of buttons a dialog box should display.
    /// </summary>
    public enum DialogBoxButton {
        /// <summary>
        /// Describes that only an ok button is displayed.
        /// </summary>
        OK,
        /// <summary>
        /// Describes that a yes and a no button is displayed.
        /// </summary>
        YesNo
    }
}
